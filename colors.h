#define COL_FLGRN "\e[92m"
#define COL_FLYEL "\e[93m"
#define COL_FLBLU "\e[94m"
#define COL_FLMAG "\e[95m"
#define COL_FLCYA "\e[96m"
#define COL_RS "\e[0m"

#endif
